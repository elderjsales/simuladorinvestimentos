public class TaxasHelper {
    
    public TaxasHelper(){
        
    }
    
    private String selic;
    private String ipca;
    private String prefixado;
    private String lci;
    private String lca;
    private String cdb;
    private String lc;

    public String getSelic() {
        return selic;
    }

    public void setSelic(String selic) {
        this.selic = selic;
    }

    public String getIpca() {
        return ipca;
    }

    public void setIpca(String ipca) {
        this.ipca = ipca;
    }

    public String getPrefixado() {
        return prefixado;
    }

    public void setPrefixado(String prefixado) {
        this.prefixado = prefixado;
    }

    public String getLci() {
        return lci;
    }

    public void setLci(String lci) {
        this.lci = lci;
    }

    public String getLca() {
        return lca;
    }

    public void setLca(String lca) {
        this.lca = lca;
    }

    public String getCdb() {
        return cdb;
    }

    public void setCdb(String cdb) {
        this.cdb = cdb;
    }

    public String getLc() {
        return lc;
    }

    public void setLc(String lc) {
        this.lc = lc;
    }
    
    
    
}
